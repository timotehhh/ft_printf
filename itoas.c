#include "lib.h"

unsigned int	ft_uatoi(char *nbr)
{
	unsigned int	nb;
	int				i;

	i = -1;
	nb = 0;
	while (nbr[++i])
		nb = nb * 10 + (nbr[i] - '0');
	return (nb);
}
